package Ejercicio4;

public class Atletas {

	private String nombre;
	private int tiempo;
	private int numero;

	public Atletas() {}

	public void setTiempo(int x) {
		tiempo = x;
	}
	public void setNombre(String x) {
		nombre = x;
	}
	public void setNumero(int x) {
		numero = x;
	}
	public int getTiempo() {
		return tiempo;
	}
	public String getNombre() {
		return nombre;
	}
	public int getNumero() {
		return numero;
	}
}
